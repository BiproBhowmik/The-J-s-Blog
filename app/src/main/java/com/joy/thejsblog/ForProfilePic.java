package com.joy.thejsblog;

public class ForProfilePic {

    String pp, em;

    public ForProfilePic() {
    }

    public ForProfilePic(String pp, String em) {
        this.pp = pp;
        this.em = em;
    }

    public String getPp() {
        return pp;
    }

    public void setPp(String pp) {
        this.pp = pp;
    }

    public String getEm() {
        return em;
    }

    public void setEm(String em) {
        this.em = em;
    }
}
